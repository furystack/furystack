/**
 * The default aspect names
 */
export enum DefaultAspects {
  /**
   * Used during content creation
   */
  Create = 'Create',
  /**
   * Used for listing a collection of a specific content
   */
  List = 'List',
  /**
   * Used when you view a detailed info about a content
   */
  Details = 'Details',
  /**
   * Used during content modification
   */
  Edit = 'Edit',
  /**
   * An inline view for AutoComplete
   */
  Autocomplete = 'Autocomplete',
  /**
   * An expanded view
   */
  Expanded = 'Expanded',
}
